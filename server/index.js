const express = require("express");
const cors = require("cors");

const app = express();
app.use(cors());
app.use(express.json());

app.get("/health", (req, res) => {
  res.json({ ok: true, message: "Visitor Check-In API running" });
});

const PORT = 3001;
app.listen(PORT, () => {
  console.log(`API running at http://localhost:${PORT}`);
});
